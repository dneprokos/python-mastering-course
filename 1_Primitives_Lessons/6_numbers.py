x = 1
y = 1.1
z = 1 + 2j
print(x)  # 1
print(y)  # 1.1
print(z)  # (1+2j)
print(10 + 3)  # 13 Addition
print(10 - 3)  # 7 Subtraction
print(10 * 3)  # 30 Multiplication
print(10 / 3)  # 3.3333333333333335 Division
# 3 Floor Division 10 // 3 results in 3 (because 10 ÷ 3 is 3.333, and only the integer part 3 is kept).
print(10 // 3)
# 1 Modulus 10 % 3 results in 1, because 10 ÷ 3 gives 3 with a remainder of 1.
print(10 % 3)
# 1000 Exponentiation 10 ** 3 means 10 raised to the power of 3 (10 × 10 × 10), which results in 1000.
print(10 ** 3)

x += 3
print(x)  # 13
